

public class SwitchCase {

	public static void main(String[] args) {
		
		int g=0;
		switch(g){
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
			break;
		default :
			System.out.println("Nothing");
		}

	}

}
