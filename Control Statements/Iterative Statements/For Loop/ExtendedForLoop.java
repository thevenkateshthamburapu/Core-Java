

public class ExtendedForLoop {

	public static void main(String[] args) {
		int a[]=new int[]{1,2,3,4,5};
		//System.out.println(a[0]);
		
		//datatype container_name
		for(int i : a){
			System.out.println(i);
		}

	}

}
