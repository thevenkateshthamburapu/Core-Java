

public class TernaryOperators {

	public static void main(String[] args) {
		
		int a=2;  
		int b=5;  
		int min=(a<b)?a:b;  
		System.out.println(min); 
		
		int c=10;  
		int d=5;  
		int max=(c>d)?c:d;  
		System.out.println(max);  
		
		int i=1;
		int j=5;
		j=i==1?6:7;
		System.out.println(j);
	}

}
